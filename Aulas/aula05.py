# Tipo de dado bool (booleano)
# Ao questionar algo em um programa, só existem duas respostas possiveis:
# sim (True) ou não (False)
# Existem varios operadores para "Questionar".
# Dentre eles o ==, que é um operador lógico que questiona se um valor é igual a outro
#
#
print(10 == 10) # Sim -> True (Verdadeiro) 
print(20 ==3)   # Não -> False (Falso)

print(type(True))
print(type(False))
print(type(10 == 10))
print(type(20 ==3))
