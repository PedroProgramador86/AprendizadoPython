# Variaveis são usadas para salvar algo na memoria do computador
# PEP8: Inicie variaveis com letras minusculas, pode usar numeros e underline_.
# O sinal de = é o operador de atribuição. Ele é usado para atribuir um valor a um nome (variavel).
# Uso: Nome_variavel = expressão

meu_nome = "Pedro Enzo Lima Luna"

print(meu_nome)

soma = 2 + 2

print(soma)

nome = "Pedro"
idade = 18
maioridade = idade >= 18
print("Nome: ", nome, "Idade: ", idade, "Maior de idade ? ", maioridade)
